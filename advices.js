/* Детерминированный совет по пользователю и дате.
   По умолчанию берётся текущая дата (локальная).
*/
window.getDailyAdvice = function(user, date = new Date()) {
  const list = user === "Никося" ? window.ADVICES_GIRL_TO_GUY : window.ADVICES_GUY_TO_GIRL;
  // Ключ YYYY-MM-DD
  const d = new Date(date.getFullYear(), date.getMonth(), date.getDate());
  const key = d.toISOString().slice(0,10);
  // Хеш FNV-1a
  let h = 2166136261;
  for (let i = 0; i < key.length; i++) {
    h ^= key.charCodeAt(i);
    h = (h * 16777619) >>> 0;
  }
  return list[h % list.length];
};
